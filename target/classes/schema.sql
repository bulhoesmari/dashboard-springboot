CREATE TABLE account_details
(
   id  			  INTEGER GENERATED BY DEFAULT AS IDENTITY,
   account_number INTEGER,
   account_type 	VARCHAR (255),
   address      	VARCHAR (255),
   email        	VARCHAR (255),
   first_name   	VARCHAR (255),
   last_name    	VARCHAR (255),
   zipcode      	VARCHAR (255),
   PRIMARY KEY (id)
   );